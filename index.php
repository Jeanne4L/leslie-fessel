        <section id="job">
            <div class="section__content">
                <h2>Psychomotricité</h2>
                <div class="section__content--max">
                    <img src="images/psycho-img.png" alt="logo psychomotricité">
                    <div class="float"></div>
                    <p>
                        La psychomotricité est axée sur la prévention, le dépistage et la rééducation des troubles psychomoteurs, à tout âge de la vie. La.le psychomotricien.ne étudie les interactions entre <b>perception sensorielle, émotion, pensée, mouvement et comportement</b>.
                        <br>
                        La psychomotricité est également un <b>espace de thérapie</b> basée sur une approche <b>psycho-corporelle</b> par le mouvement, l'expression corporelle, la relaxation, les modelages... visant à rétablir les <b>liens entre le corps et le psychisme</b>.
                    </p>
                    <h3>Pour qui ?</h3>
                    <p>
                        Les séances de psychomotricité sont soumises à une obligation de prescription médicale (médecin traitant, psychiatre, pédopsychiatre...).
                        <br>
                        Les champs d'intervention en psychomotricité sont multiples :
                    </p>
                    <ul>
                        <li>retard du développement psychomoteur</li>
                        <li>troubles de la latéralité</li>
                        <li>troubles de l'organisation spatio-temporelle</li>
                        <li>troubles tonico-émotionnels (coordinations, accordage relationnel parent-enfant par exemple)</li>
                        <li>inhibition psychomotrice</li>
                        <li>agitation/instabilité psychomotrice</li>
                        <li>troubles de l'image du corps et du schéma corporel (dysmorphophobie par exemple)</li>
                        <li>anxiété, stress, angoisses</li>
                    </ul>
                    <h3>Quand faut-il consulter un.e psychomotricien.ne ?</h3>
                    <p>
                        La psychomotricité est destinée aux enfants, aux adolescents et aux adultes ayant des difficultés sur le plan sensoriel, moteur, émotionnel, relationnel et/ou comportemental.
                        <br>
                        La thérapie psychomotrice est un espace de soin pouvant accueillir la souffrance psychologique lorsqu'elle est vécue et/ou exprimée par le corps. Elle propose un espace de soin fondé sur une approche corporelle, permettant ainsi ; même lorsque les mots vous manquent, de vous reconnecter à vos ressentis internes, vos sensations et vos émotions.
                    </p>
                    <h3>Les consultations en psychomotricité</h3>
                    <p>
                        La première consultation a pour objectif <!--de créer une première rencontre afin--> de tisser les premiers liens de confiance. Cette première séance permettra également d'établir la nécessité, ou non, d'une passation de bilan psychomoteur, de cerner votre demande et d'élaborer les objectifs des séances. Je pourrai ainsi adapter au mieux le cadre des consultations et mes propositions de soins.
                        <br>
                        Le bilan psychomoteur se déroule sur 3 séances d'une heure avec une séance de restitution des résultats (ainsi qu'un compte-rendu écrit). Il a pour but d'analyser le développement psychomoteur de la personne au travers de différents items comme les coordinations, l'équilibre, la latéralité, le tonus, l'espace et le temps, le schéma corporel et l'image du corps. Le bilan psychomoteur peut constituer une première étape diagnostique dans le repérage d'un retard de développement ou de troubles des apprentissages. Les résultats du bilan permettront d'affiner les indications de suivi en psychomotricité ou d'une réorientation thérapeutique.
                    </p>
